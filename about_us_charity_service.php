                    <h2>THE MERCY MINISTRY</h2>
                    <p>Our goal is not only to feed people, but also to teach them how to overcome poverty. Oftentimes, poor life is the result of a wrong, poverty mentality. Instead of thinking of how to overcome their need, poor people think that everybody owes to them and always wait for others to help them. They never think about how they can be of use to others.
                        <br>
                        The biblical principle ‘give and it shall be given to you’ works in this world. That is why it is important to help people to get rid of poverty mentality, to start thinking right about their life and future and to take right actions.
                    </p>
                    <p>Today many individuals and organizations do charities. Some are involved in charities for conscience sake, others to build up a reputation or to be known as wealthy benefactors. However, so many people do nothing, thinking that one needs a lot of money to do works of mercy. The true charity, on the other hand, must outflow not from financial abundance or desire to be known as a benefactor, but from compassion.
If we wait until we have enough money to do works of mercy, that time may not come at all. Money always comes after hearing the voice of God and obeying it. Unless you step into the Jordan, waters will not divide.
The basis of charity must be the commandment of God. Jesus said: “I was hungry and you gave Me food; I was thirsty and you gave Me drink… I was naked and you clothed Me…” (Matthew 25:35-36)
</p>
                    <p>Success of the Mercy Ministry depends mostly not on finances, but on the staff. Jesus Christ said: “The harvest truly is plentiful, but the laborers are few. Therefore pray the Lord of the harvest to send out laborers into His harvest.” (Matthew 9:37-38)
It was not difficult for Jesus to feed more than 5.000 people; He did a great miracle of multiplication of food and distributed it to people, as there were people by His side who assisted Him. A young boy offered his five loaves and two fish, and the disciples made people sit down in groups of fifty, keeping the order. After that, the miracle took place.
The Mercy Ministry of our Church started with a Matnakash bread offered by a woman. The bread was divided between four needy people. After this instance, the Ministry grew and expanded, and the Church was able to carry out many programs feeding the hungry, reaching the homeless, providing humanitarian aid to borderline villages, helping lonely old people, disabled people and children who became homeless.
</p>
                    <p>We do not do charities to exalt ourselves or to increase the number of the members of the Church. We never seek what is ours; we do what the Lord tells us. We just show God’s love to people trying to help them the way we can. People themselves ask us: “Are you believers? Only God’s children can love and care in such a way.” That’s when we remember Jesus’ words: “…that they may see your good works and glorify your Father in heaven.” (Matthew 5:16)</p>
                    <p>The Church must not be turned into a humanitarian organization. If we think that people will not come to God unless we give aid to them, we are wrong. The Word of God does not command us to go around the world and do charities, but to go and preach the Gospel. Thus, the primary task of any Church must be to spread the Gospel, and the major part of the finances must serve this purpose. No matter how much you help, feed and clothe people, their lives will not be changed if they have not come to know God.
We had such an experience while working with homeless people. Those of them, who received Christ, had their lives changed: they got a job, acquired a house, and built a family. Those who did not receive Christ returned to their former life.</p>
                    <p>In the Early Church, there was no one among believers who lacked anything. Whoever had lands or houses, sold them, brought the money and put before the feet of the Apostles. In this way, each received whatever they needed. It does not mean that the Word of God urges all to sell everything and to give the gain to the poor. It simply means that there were wealthy people in the Church, who were ready to offer their possessions for God’s work. It is not written, however, that they gave their last. This means that charity work requires cautiousness. In order not to fall into extremes, it is very important not to start spontaneous activities, but to do what God tells us in His Word.</p>
                    <p>Many people think that charity means only material support. Actually, it is about more. Oftentimes, people do not need money, but attention; they need someone who will lift them up and will help to move on.</p>
                    <p>     When we started to work with homeless children, we found out that many of them had parents, but their parents rejected them. These children needed parents’ heart, care and attention. We decided to take them from the streets, to take care of them, to keep them from bad companies and poverty, and to return them to their parents. At the same time, we worked with their parents, so that they could experience change and be able to build wonderful families. And we succeeded!</p>
