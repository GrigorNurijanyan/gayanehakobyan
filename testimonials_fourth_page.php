                    <p>
                    My family experienced a great tragedy; three years ago, my daughter-in-law was diagnosed with liver cancer. She and my son were a young couple, who were raising their little children in an atmosphere of love and warmness. However, that disease shattered the peace of our family.
                    </p>
                    <p>
                    In hope to save his wife, my son took her abroad. Since we had no sufficient means, he put our apartment in pledge. With the money received, they left for Germany. Unfortunately, the doctors there could not save this young woman, and this story ended with tragedy. 
                    </p>
                    <p>
                    My three little grandchildren and I were in a hopeless situation. The children took hard the loss of their mother and the absence of their father. For more than a year, our family was desperate both mentally and financially. Everything seemed to turn upside down. In order to meet the ends and repay the big loan taken for the medical expenses, my son had to stay in Germany to work and support us.
                    </p>
                    <p>
                    Praise God, in that period, “Go and See…” Social Ministry workers extended a helping hand to me and shared my burden. I started to notice a change in the children day after day. In the Center, teachers and psychologists worked with them. My grandchildren were involved in all the study groups and every time attended the Development Center with much enthusiasm. Before that, they used to sit at home and did not even want to go outside to play.
                    </p>
                    <p>
                    Sadness and distress were leaving us day by day. Children wanted to study again. They became bolder and more self-dependent. They were filled with new and big dreams. As a grandmother, I felt happy. A period of blessings started in our family, and I am very grateful to the Lord for that.
                    </p>
                    <p>
Life circumstances may not turn the way we expect. However, even after a disastrous loss, one must not lose hope, but cling to the Living God, Who will accompany them in this difficult path to a victorious end. No one will remain in an impossible situation if their hope is God.
                    </p>
