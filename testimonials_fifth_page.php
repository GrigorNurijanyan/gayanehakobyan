                    <p>
                    My name is Mnatsakan Yesayan, I live in Yerevan with my father, grandmother and brother, whose name is Gagik. When I was five and my brother was three, our mother left us, and our grandmother was the one who cared for us. My father has a serious mental illness, so he was unable to take good care of us and bring us up.
                    </p>
                    <p>
                    We do not blame our mother; but oftentimes, we felt the lack of her love and warmth, which no one could replace. My grandmother did everything for us as much as she could, but anyway, in our life, there were many gaps, especially in education.
                    </p>
                    <p>
                    The main concern of my grandmother was to feed us, so that we did not go to bed hungry; our education was not much important for her. Attending school became a burden for us; we were always unprepared for lessons.  School became the most disgusting place in the world for us; we did not like to study, and were lagging behind the school curriculum. In school, teachers ignored us. Because we were not able to learn, they simply asked us to be silent during the lessons, so that in the end, putting formal satisfactory grades, they could just move us to the next form.
                    </p>
                    <p>
                    I was already in the 7th form, and my brother was in the 5th, but we read in Armenian with much difficulty, still confusing some letters; we did not know the multiplication table, and I don’t even speak about foreign languages. We got used to the title of “the laziest pupil in the class” in our classes. We were sure that nothing could change our situation.
                    </p>
                    <p>
                    Soon my grandmother started to attend the Word of Life Church. She learned about the Development Center and asked them to involve us as well. We liked the Center from the very first day; we studied with much difficulty, but the dedication and love of the teachers toward us made us start everything anew.
                    </p>
                    <div class="multiple-image-wrapper">
                        <img src="./assets/older-garden-background.png" alt="Image 1">
