                    <p>
                    Today we can read and write not only in Armenian, but also in Russian and English. In the Development Center, we understood that we are not orphans; we have a Heavenly Father, Who loves us and cares for us. We also understood that it was not late for us, and if we tried hard, we would succeed, because nothing is impossible. We attended the Center every day. The specialists worked with us, so that we could catch up with our classmates, because we were way behind them. Today we make a good progress in school; we like to attend school and eagerly prepare our lessons.
                    </p>
                    <p>
                    In the Development Center, we receive comprehensive education: we study puppet-making and bas-relief; we acquire knowledge about the world around us; we take part in theatrical performances; we participate in many interesting events – things we were always deprived of.
                    </p>
                    <p>
                    In the Center, the course of our life and our worldview have been changed. Today we are happy. Through faith in God, we dream about greater things and wait for the wonderful day when we will see our big dreams come true. We thank God for giving us this opportunity!
