                    <h2>THE KINGDOM OF HEAVEN BELONGS TO CHILDREN</h2>
                    <p>In 2001-2010, we carried out ‘The Kingdom of Heaven to Children’ program, which consists of the following stages: Prevention, Orientation, and Foster Care.</p>
                    <p>At Prevention stage, the Mercy Ministry Professional Team (a psychologist, a social worker, a pedagogue, and volunteers) prevents the child from getting into an orphanage.</p>
                    <p>At Orientation stage, the Professional Team discusses the family situation of the child (relationships between the family members, social conditions, risks to the child’s safety), and the results determine whether the child will continue to live with their biological family, or with a foster family.</p>
                    <p>At Foster Care stage, the child temporarily lives in a foster family, where their safety (if they were abused in their biological family), proper social conditions and education are secured. At the same time, the Mercy Ministry team works with their parents, so that in future the child could have opportunity to return to their biological family.</p>
